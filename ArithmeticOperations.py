#3.Arithmetic Operations
a = int(input("Enter a:"))
b = int(input("Enter b:"))

# Addition
sum = a + b
print("Sum:", sum)

# Subtraction
difference = a - b
print("Difference:", difference)

# Multiplication
product = a * b
print("Product:", product)

# Division
quotient = a / b
print("Quotient:", quotient)

# Modulo (Remainder)
remainder = a % b
print("Remainder:", remainder)